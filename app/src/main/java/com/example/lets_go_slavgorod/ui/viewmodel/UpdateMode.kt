/**
 * Режимы обновлений приложения
 * 
 * Определяет, как приложение должно проверять и устанавливать обновления:
 * - AUTOMATIC: автоматическая проверка и уведомления об обновлениях
 * - MANUAL: только ручная проверка обновлений
 * - DISABLED: отключение всех проверок обновлений
 */
package com.example.lets_go_slavgorod.ui.viewmodel

/**
 * Режимы обновлений приложения
 */
enum class UpdateMode {
    /**
     * Автоматическая проверка обновлений
     * Приложение будет автоматически проверять обновления в фоне
     * и уведомлять пользователя о доступных обновлениях
     */
    AUTOMATIC,
    
    /**
     * Ручная проверка обновлений
     * Пользователь может проверить обновления только вручную
     * через настройки приложения
     */
    MANUAL,
    
    /**
     * Отключение проверки обновлений
     * Приложение не будет проверять обновления
     */
    DISABLED
}
